import { writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..');
const outputPath = path.join(rootDir, 'app-config.js');

const inputUrl = process.argv[2] || process.env.DAPOERMUDA_API_BASE_URL || '';
const normalizedApiBaseUrl = String(inputUrl).trim().replace(/\/+$/, '');
const timeoutInput = process.env.DAPOERMUDA_REQUEST_TIMEOUT_MS || '12000';
const requestTimeoutMs = Math.max(4000, Number(timeoutInput) || 12000);

const lines = [
  '// Generated by scripts/write-app-config.mjs',
  '// Owner can set DAPOERMUDA_API_BASE_URL before building web or Android assets.',
  'window.DAPOERMUDA_APP_CONFIG = Object.assign({',
  `  apiBaseUrl: ${JSON.stringify(normalizedApiBaseUrl)},`,
  `  requestTimeoutMs: ${requestTimeoutMs}`,
  '}, window.DAPOERMUDA_APP_CONFIG || {});',
  ''
];

await writeFile(outputPath, lines.join('\n'), 'utf8');
console.log(`app-config.js updated (${normalizedApiBaseUrl || 'same-origin'})`);
